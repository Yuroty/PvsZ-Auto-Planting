DELAY = 0.05  # seconds between clicks, adjust as needed
mouse = Controller()
start_replay = False

def on_press(key):
    global start_replay
    try:
        if key.char == '2':
            start_replay = True
            return False
    except AttributeError:
        pass

print("Press '2' to start replaying clicks...")
with keyboard.Listener(on_press=on_press) as listener:
    listener.join()

print(f"Starting replay of {len(clicks)} clicks now!")

for x, y in clicks:
    mouse.position = (x, y)
    mouse.click(Button.left, 1)
    print(f"Clicked at ({x}, {y})")
    time.sleep(DELAY)

